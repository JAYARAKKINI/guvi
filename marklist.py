#Grade for marks                                                                                                                                                                                                                                                                                        

mark=int(input('Enter your marks:'))  #Enter the mark to find grade
if(mark>=90):
  print('S Grade')
elif(mark>=80):
  print('A Grade')
elif(mark>=60):
  print('B Grade')
elif(mark>=40):
  print('C Grade')
else:
  print('FAIL')  
